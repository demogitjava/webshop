package de.jgsoftware.webshop.model;


/**
 *
 * @author hoscho
 */
public class EbayKeys
{
    public static String ebayappid;
    public static String ebaytoken;
    public static String certid;

    public String getEbayappid() {
        return ebayappid;
    }
    public void setEbayappid(String ebayappid) {
        this.ebayappid = ebayappid;
    }
    public String getEbaytoken() {
        return ebaytoken;
    }
    public void setEbaytoken(String ebaytoken) {
        this.ebaytoken = EbayKeys.ebaytoken;
    }
    public String getCertid() {
        return certid;
    }
    public void setCertid(String certid) {
        this.certid = certid;
    }
}
